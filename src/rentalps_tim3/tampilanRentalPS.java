    private void btnProsesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProsesActionPerformed
        double tagihan;
        tagihan = Integer.parseInt(txtHarga.getText()) * (int)sDurasi.getValue();
        txtTagihan.setText(String.valueOf(tagihan));
        LocalTime jamKeluar = jamMasuk.plusHours((int)sDurasi.getValue()).plusMinutes(0);
        String jamKeluarString = FOMATTER.format(jamKeluar);
        txtJamKeluar.setText(jamKeluarString);
        
        // Proses mencatat transaksi ke database
        Koneksi conn = new Koneksi();
        String jenis_ps = String.valueOf(cbJenisPs.getSelectedItem());
        int durasi = Integer.parseInt(String.valueOf(sDurasi.getValue()));
        String no_meja = String.valueOf(cbMeja.getSelectedItem());
        
        String query =  "insert into transaksi (jenis_ps, durasi, no_meja, jam_masuk, jam_keluar, total_tagihan) values ('" 
                + jenis_ps + "', " + durasi + ", '" + no_meja + "', '" + localTimeString + "', '" + jamKeluarString + "', " + tagihan + ")";
        try{
            conn.getStatement().executeUpdate(query);
            JOptionPane.showMessageDialog(null, "Transaksi berhasil dicatat! \nSelamat bermain!", "Information", JOptionPane.INFORMATION_MESSAGE);   
        }
        catch(SQLException ex){
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(null, "Yah! Transaksi gagal dicatat. \nMohon coba lagi nanti.");
        }
    }//GEN-LAST:event_btnProsesActionPerformed

