    private void btnRefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRefreshActionPerformed
        // TODO add your handling code here:
        Koneksi conn = new Koneksi();
        ResultSet data = null;
        int jumBaris = 0;
        int i = 0;
        String query = "SELECT * FROM transaksi";
        try{
            data = conn.getStatement().executeQuery(query);
            
            while(data.next()){
                jumBaris++;
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "ERROR");
        }
        
        String isi[][] = new String[jumBaris][7];
        
        try{
            data = conn.getStatement().executeQuery(query);
            
            while(data.next()){
                isi[i][0] = data.getString("no_transaksi");
                isi[i][1] = data.getString("jenis_ps");
                isi[i][2] = data.getString("durasi");
                isi[i][3] = data.getString("no_meja");
                isi[i][4] = data.getString("jam_masuk");
                isi[i][5] = data.getString("jam_keluar");
                isi[i][6] = data.getString("total_tagihan");
                i++;
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Error lagi");
        }
        
        String namaKolom[] = {"No. Transaksi", "Jenis PS", "Durasi Main", "No. Meja", "Jam Masuk", "Jam Keluar", "Total Tagihan"};
        DefaultTableModel model = new DefaultTableModel(isi, namaKolom){};
        jTable1.setModel(model);
    }//GEN-LAST:event_btnRefreshActionPerformed

